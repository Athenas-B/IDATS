    private void btnUlozActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUlozActionPerformed
        JFileChooser fc = new JFileChooser();
        int returnVal = fc.showSaveDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                //System.out.println(fc.getSelectedFile().getPath());
                BufferedWriter out = new BufferedWriter(new FileWriter(fc.getSelectedFile().getPath()));
                String ln = System.getProperty("line.separator");
                String text = txtaObsah.getText();
                text = text.replaceAll("\n", ln);
                out.write(text);
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(ProgObyvatele.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            //log.append("Open command cancelled by user." + newline);
        }
    }//GEN-LAST:event_btnUlozActionPerformed

