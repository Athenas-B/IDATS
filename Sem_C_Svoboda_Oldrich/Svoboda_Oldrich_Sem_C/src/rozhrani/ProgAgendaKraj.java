    private void btnGenerujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerujActionPerformed
        txtaObsah.setText(null);
        agenda = new AgendaKraj();
        obyvatele = new Obyvatele();
        int pocetZaznamu = 0;
        int pocetKraju = Generator.generujCislo(2, 20);
        for (int kraj = 1; kraj <= pocetKraju; kraj++) {
            String nazevKraje = Generator.generujNazev();
            agenda.vytvorKraj(nazevKraje);
            obyvatele.vytvorKraj(nazevKraje);
            int pocetObci = Generator.generujCislo(2, 10);
            pocetZaznamu += pocetObci;
            for (int obec = 0; obec < pocetObci; obec++) {
                IObec nova = Generator.generujObec(pocetKraju);
                nova.setKraj(kraj);
                agenda.Vloz(nova);
                obyvatele.vlozObec(nova, enumPozice.POSLEDNI, kraj);
            }
        }
        txtaObsah.setText("vygenerovano " + pocetKraju + " kraju, " + pocetZaznamu + " obci");

    }//GEN-LAST:event_btnGenerujActionPerformed

